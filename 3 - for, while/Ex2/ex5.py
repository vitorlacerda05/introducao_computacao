import math

#Atribuição de valores para as variáveis
chico = 150
ze = 110

#Impressão dos valores iniciais das variáveis
print("Altura do Chico: {} cm".format(chico))
print("Altura do ze: {} cm".format(ze))
print("")

#Comando while até zé ser maior que chico, imprimindo os valores
while ze <= chico :
	chico = chico + 2
	print("Altura do Chico: {} cm".format(chico))
	
	ze = ze + 3
	print("Altura do Zé: {} cm".format(ze))
	print("")
